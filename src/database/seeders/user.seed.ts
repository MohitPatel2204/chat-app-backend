const existUser = async (username: string, email: string) => {
  return await User.findOne({ where: { username, email } });
};

const createUsers = async () => {
  db.connect();
  try {
    const roles = await Role.findAll();

    for (const role of roles) {
      console.log(` ${role.name} role users seeding start...`);
      const data = users[role.name as keyof typeof users];
      for (const user of data) {
        if (!(await existUser(user.username, user.email))) {
          await User.create({
            ...user,
            gender:
              user.gender.toLocaleLowerCase() as unknown as typeof genderEnum,
            roleId: role.id,
            isActive: true,
            password: getHashPassword(user.password),
          });
        }
      }
      console.log(` ${role.name} role users seeding complete...`);
    }
  } catch (error) {
    throw new Error((error as Error)?.message);
  }
};

console.log(" Users seeder running...");
createUsers()
  .then(() => {
    console.log(" Users seeder successfully completed");
    process.exit(0);
  })
  .catch((error) => {
    console.error(" Error seeding users: ", error);
    process.exit(0);
  });
